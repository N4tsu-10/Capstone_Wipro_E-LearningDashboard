// {
//     context.Request.Scheme = "https";
//     context.Request.Host = new HostString(context.Request.Host.Host, httpsPort);
//     return next();
// });



app.UseRouting();
app.UseCors("AllowAll");

app.UseAuthentication();
app.UseAuthorization();

app.MapControllers();

app.Run();